            <h2>BBQ Deep Pan Pizza</h2>
            <p>Author: BBQ Chef | Date: December 4, 2023</p>
            <p>Ervaar de onweerstaanbare smaak van een diepe pan pizza bereid op de barbecue. Een waar festijn voor de liefhebbers van hartige, rokerige smaken.</p>
            <h3>Ingrediënten:</h3>
            <ul>
                <li>1 portie pizzadeeg (zelfgemaakt of kant-en-klaar)</li>
                <li>Tomatensaus naar smaak</li>
                <li>Geraspte mozzarella kaas (of een mix van favoriete kazen)</li>
                <li>Verse basilicumblaadjes</li>
                <li>Olijfolie</li>
                <li>Zout en peper naar smaak</li>
                <li>Optioneel: Toppings zoals champignons, paprika, ui, en meer</li>
            </ul>
            <h3>Bereiding:</h3>
            <ol>
                <li>Verwarm de BBQ voor op een gematigde temperatuur (200-225 graden Celsius) met directe warmte.</li>
                <li>Rol het pizzadeeg uit en plaats het in een ingevette diepe pan.</li>
                <li>Spreid een royale laag tomatensaus uit over het deeg.</li>
                <li>Strooi de mozzarella gelijkmatig over de saus en voeg je favoriete toppings toe.</li>
                <li>Plaats de pan op de BBQ, sluit het deksel en bak tot de korst goudbruin is en de kaas smelt (15-20 minuten).</li>
                <li>Haal de pizza voorzichtig van de BBQ, bestrooi met basilicum, besprenkel met olijfolie en voeg zout en peper toe.</li>
                <li>Snijd in punten en serveer direct. Geniet van elke smakelijke hap!</li>
            </ol>
        </article>


            <h2>Pizza Truffel</h2>
            <p>Author: BBQ Chef | Date: November 19, 2023</p>
            <p>Ontdek het overheerlijke recept voor pizza truffel en geniet van de unieke smaak van truffel op je pizza.</p>
            <h3>IngrediÃ«nten:</h3>
            <ul>
                <li>500 gram pizzadeeg</li>
                <li>150 ml tomatensaus</li>
                <li>200 gram mozzarella, in plakjes</li>
                <li>50 gram Parmezaanse kaas, geraspt</li>
                <li>50 gram champignons, in plakjes</li>
                <li>1 truffel, dun gesneden</li>
                <li>Zout en zwarte peper naar smaak</li>
                <li>Olijfolie</li>
                <li>Verse basilicumblaadjes voor garnering</li>
            </ul>
            <h3>Bereiding:</h3>
            <ol>
                <li>Verwarm de oven voor volgens de instructies van het pizzadeeg.</li>
                <li>Rol het pizzadeeg uit op een met bloem bestoven oppervlak.</li>
                <li>Bestrijk het deeg met tomatensaus, laat de randen vrij voor de korst.</li>
                <li>Beleg met plakjes mozzarella, champignons en dunne plakjes truffel.</li>
                <li>Strooi Parmezaanse kaas over de pizza en breng op smaak met zout en zwarte peper.</li>
                <li>Bak de pizza in de voorverwarmde oven volgens de instructies van het pizzadeeg.</li>
                <li>Haal de pizza uit de oven, besprenkel met olijfolie en garneer met verse basilicumblaadjes.</li>
                <li>Snijd en serveer de heerlijke pizza truffel.</li>
                <li>Geniet van deze smaakvolle truffelpizza!</li>
            </ol>

            <h2>Gevulde Ananas met Kip Teriyaki</h2>
            <p>Author: BBQ Chef | Date: November 19, 2023</p>
            <p>Ontdek dit heerlijke recept voor gevulde ananas met sappige kip Teriyaki voor een unieke en smaakvolle barbecue-ervaring.</p>
            <h3>IngrediÃ«nten:</h3>
            <ul>
                <li>2 verse ananassen</li>
                <li>500 gram kipfilet, in blokjes</li>
                <li>1 kopje Teriyaki saus</li>
                <li>1 rode paprika, in blokjes</li>
                <li>1 ui, fijngehakt</li>
                <li>2 teentjes knoflook, fijngehakt</li>
                <li>Zout en zwarte peper naar smaak</li>
                <li>Verse koriander, gehakt, voor garnering</li>
            </ul>
            <h3>Bereiding:</h3>
            <ol>
                <li>Snijd de bovenkant van de ananassen af en hol ze voorzichtig uit.</li>
                <li>Marineer de kipblokjes in Teriyaki saus en laat het minstens 30 minuten rusten.</li>
                <li>Verhit wat olie in een pan en bak de gemarineerde kip tot het gaar is.</li>
                <li>Voeg de rode paprika, ui en knoflook toe aan de pan en bak tot de groenten zacht zijn.</li>
                <li>Vul de uitgeholde ananassen met het kip Teriyaki mengsel.</li>
                <li>Gril de gevulde ananassen op de barbecue tot ze mooi zijn gekarameliseerd.</li>
                <li>Garneer met verse koriander en serveer deze smaakvolle gevulde ananas met kip Teriyaki.</li>
                <li>Geniet van deze exotische BBQ-sensatie!</li>
            </ol>

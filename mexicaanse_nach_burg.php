            <h2>Mexicaanse Nacho Burger</h2>
            <p>Author: BBQ Chef | Date: November 19, 2023</p>
            <p>Probeer deze smaakvolle Mexicaanse Nacho Burger voor een pittige en heerlijke barbecue-ervaring.</p>
            <h3>Ingrediënten:</h3>
            <ul>
                <li>500 gram rundergehakt</li>
                <li>1 zak nachochips</li>
                <li>150 gram cheddarkaas, geraspt</li>
                <li>1 rode ui, fijngehakt</li>
                <li>1 jalapeño, fijngehakt</li>
                <li>1 theelepel chilipoeder</li>
                <li>1 theelepel komijnpoeder</li>
                <li>Zout en zwarte peper naar smaak</li>
                <li>Guacamole en zure room voor het serveren</li>
            </ul>
            <h3>Bereiding:</h3>
            <ol>
                <li>Meng het rundergehakt met de fijngehakte rode ui, jalapeño, chilipoeder, komijnpoeder, zout en zwarte peper.</li>
                <li>Vorm het gehaktmengsel tot burgers.</li>
                <li>Gril de burgers op de barbecue tot ze gaar zijn.</li>
                <li>Leg de nachochips op een serveerschaal en strooi de geraspte cheddarkaas eroverheen.</li>
                <li>Plaats de gegrilde burgers op de nachochips en kaas.</li>
                <li>Garneer met guacamole en zure room.</li>
                <li>Serveer deze Mexicaanse Nacho Burger als een heerlijke en pittige traktatie!</li>
            </ol>

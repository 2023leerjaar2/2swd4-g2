            <h2>Brownies met Pecannoten op de Kamado BBQ</h2>
            <p>Author: BBQ Chef | Date: [Vul de datum in]</p>
            <p>Geniet van heerlijke, smeuïge brownies met een knapperige textuur van pecannoten, bereid op de Kamado BBQ. Een perfect dessert voor elke gelegenheid.</p>
            <h3>Ingrediënten:</h3>
            <ul>
                <li>200 gram boter</li>
                <li>200 gram donkere chocolade, in stukjes</li>
                <li>250 gram suiker</li>
                <li>4 eieren</li>
                <li>1 theelepel vanille-extract</li>
                <li>150 gram bloem</li>
                <li>1/2 theelepel bakpoeder</li>
                <li>1/4 theelepel zout</li>
                <li>150 gram gehakte pecannoten</li>
            </ul>
            <h3>Bereiding:</h3>
            <ol>
                <li>Verwarm de Kamado BBQ voor op 180 graden Celsius.</li>
                <li>Bekleed een bakvorm met bakpapier.</li>
                <li>Smelt de boter en chocolade au bain-marie tot een glad mengsel.</li>
                <li>Klop in een aparte kom suiker, eieren en vanille-extract tot een luchtig mengsel.</li>
                <li>Voeg het chocolademengsel toe aan het suikermengsel en meng goed.</li>
                <li>Zeef de bloem, bakpoeder en zout boven het mengsel en vouw voorzichtig in.</li>
                <li>Voeg de gehakte pecannoten toe en meng gelijkmatig.</li>
                <li>Giet het beslag in de bakvorm en verspreid het gelijkmatig.</li>
                <li>Bak de brownies ongeveer 25-30 minuten in de voorverwarmde Kamado BBQ.</li>
                <li>Laat de brownies afkoelen, snijd in vierkanten en serveer.</li>
                <li>Geniet van deze verrukkelijke brownies met pecannoten!</li>
            </ol>

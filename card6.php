                <li>3 eetlepels olijfolie</li>
                <li>2 teentjes knoflook, fijngehakt</li>
                <li>1 theelepel paprikapoeder</li>
                <li>Zout en peper naar smaak</li>
                <li>Verse citroenpartjes voor het serveren</li>

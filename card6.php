            <h3>Instructies:</h3>
            <ol>
                <li>Meng in een kom de olijfolie, fijngehakte knoflook, paprikapoeder, zout en peper.</li>
                <li>Voeg de garnalen toe aan de marinade en zorg ervoor dat ze gelijkmatig bedekt zijn. Laat ze minstens 30 minuten marineren.</li>
                <li>Verwarm de barbecue voor op middelhoog vuur.</li>
                <li>Rijg de gemarineerde garnalen op spiesjes.</li>
                <li>Gril de garnalenspiesjes ongeveer 2-3 minuten per kant, of tot ze gaar zijn en een roze kleur hebben.</li>
                <li>Serveer de gegrilde garnalen met verse citroenpartjes.</li>
                <li>Geniet van je heerlijke gegrilde garnalen van de BBQ!</li>
